class Address {
  String name;
  String zipCode;
  double latitude;
  double longitude;
  String number;
  String street;
  String district;
  String city;
  String state;
  String complement;

  Address({
    this.zipCode,
    this.latitude,
    this.longitude,
    this.number,
    this.street,
    this.district,
    this.city,
    this.state,
    this.complement,
    this.name,
  });
}
